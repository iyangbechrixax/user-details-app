# Program to take user's name, age, and location and print a personalized message.

# Asking the user for their name and storing it in the variable 'name'
name = input("What is your name? ")

# Asking the user for their age and storing it in the variable 'age'
age = input("How old are you? ")

# Asking the user for their location and storing it in the variable 'location'
location = input("Where do you live? ")

# Printing out a personalized message with the user's details
print(f"Hello {name}, you are {age} years old and live in {location}.")
